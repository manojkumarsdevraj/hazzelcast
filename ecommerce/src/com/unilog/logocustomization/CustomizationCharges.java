package com.unilog.logocustomization;

public class CustomizationCharges {
	private double artCost;
	private double decoratorCost;
	private double setupCost;
	public double getArtCost() {
		return artCost;
	}
	public void setArtCost(double artCost) {
		this.artCost = artCost;
	}
	public double getDecoratorCost() {
		return decoratorCost;
	}
	public void setDecoratorCost(double decoratorCost) {
		this.decoratorCost = decoratorCost;
	}
	public double getSetupCost() {
		return setupCost;
	}
	public void setSetupCost(double setupCost) {
		this.setupCost = setupCost;
	}
	
}
