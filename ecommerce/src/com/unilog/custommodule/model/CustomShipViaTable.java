package com.unilog.custommodule.model;

public class CustomShipViaTable {
	
	private String shipViaCode;
	private String shipViaName;
	private String displaySeq;
	private String shipCode;
	private String shippingCost;
	private String siteName;
	public String getShipViaCode() {
		return shipViaCode;
	}
	public void setShipViaCode(String shipViaCode) {
		this.shipViaCode = shipViaCode;
	}
	public String getShipViaName() {
		return shipViaName;
	}
	public void setShipViaName(String shipViaName) {
		this.shipViaName = shipViaName;
	}
	public String getDisplaySeq() {
		return displaySeq;
	}
	public void setDisplaySeq(String displaySeq) {
		this.displaySeq = displaySeq;
	}
	public String getShipCode() {
		return shipCode;
	}
	public void setShipCode(String shipCode) {
		this.shipCode = shipCode;
	}
	public String getShippingCost() {
		return shippingCost;
	}
	public void setShippingCost(String shippingCost) {
		this.shippingCost = shippingCost;
	}
	public String getSiteName() {
		return siteName;
	}
	public void setSiteName(String siteName) {
		this.siteName = siteName;
	}
}
