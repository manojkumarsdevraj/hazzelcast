package com.unilog.geolocator.service.google.model;

public class LocationCoordinates {

	private Double lat;
	private Double lng;

	public Double getLat() {
		return lat;
	}

	public Double getLng() {
		return lng;
	}

	public void setLat(Double lat) {
		this.lat = lat;
	}

	public void setLng(Double lng) {
		this.lng = lng;
	}
}
